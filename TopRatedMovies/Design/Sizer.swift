import UIKit

/// Define all the values that app uses for fixed size UI elements
///
/// This struct has been defined in order to define sizing that is used in the app theme and make it
/// easy to change the theme of the app in future.
struct Sizer {
    static let sm: CGFloat  = 16
    static let md: CGFloat  = 24
    static let lg: CGFloat  = 32
    static let xlg: CGFloat  = 40
}
