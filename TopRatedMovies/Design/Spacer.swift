import UIKit

/// Define all the values that app uses for spacing between UI elements
///
/// This struct has been defined in order to define spacings that is used in the app theme and make it
/// easy to change the theme of the app in future.
struct Spacer {
    static let sm: CGFloat  = 4
    static let md: CGFloat  = 8
    static let lg: CGFloat  = 16
    static let xlg: CGFloat  = 24
    static let xxlg: CGFloat  = 32

}
