import UIKit

/// Define all the values that app uses for corner radiuses of UI elements
///
/// This struct has been defined in order to define sizing that is used in the app theme and make it
/// easy to change the theme of the app in future.
struct CornerRadius {
    static let sm: CGFloat  = 4
    static let md: CGFloat  = 8
    static let lg: CGFloat  = 12
    static let xlg: CGFloat  = 16
}
